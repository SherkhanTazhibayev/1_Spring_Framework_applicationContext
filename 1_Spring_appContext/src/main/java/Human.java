public class Human {
    private String name;
    private String gender;
    private int year;

    public Human() {

    }

    public String getName() {
        return name;
    }

    public String isGender() {
        return gender;
    }

    public int getYear() {
        return year;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
